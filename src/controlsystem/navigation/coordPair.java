package controlsystem.navigation;

public class coordPair {
	
	private int x;
	private int y;
	
	//coordPair Constructor
	public coordPair() {
		
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public void setY(int y) {
		this.y = y;
	}

}
